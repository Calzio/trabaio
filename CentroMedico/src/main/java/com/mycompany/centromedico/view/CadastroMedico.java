    private void btnRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoverActionPerformed
        int linha = tbLista.getSelectedRow();// SELECIONA A LINHA

        //CASO NÃO SELECIONE A LINHA 
        if (linha == -1) {
            JOptionPane.showMessageDialog(null, "Selecione uma pessoa para remover.");
            return;

        }

        ArrayList<Medico> Lista = controller.listar();
        Lista.remove(linha);
        atualizarTabela();
        LimparCampos();
        JOptionPane.showMessageDialog(null, "Animal removido com sucesso");
    }//GEN-LAST:event_btnRemoverActionPerformed

    private void atualizarTabela() {
        modeloTabela.setRowCount(0); // Limpa
        for (Medico m : controller.listar()) {
            modeloTabela.addRow(new Object[]{m.getNome(), m.getCrm(), m.getEspec(),m .getId()});

        }
    }

    private void LimparCampos() {
        txtNome.setText("");
        txtEspecialidade.setText("");
        txtBuscar.setText("");
        txtCrm.setText("");
        tbLista.clearSelection();
    }

    private void filtrarTabela() {
        String termo = txtBuscar.getText().trim().toLowerCase();
        modeloTabela.setRowCount(0);

        ArrayList<Medico> listaOriginal = controller.listar();

        for (Medico m : listaOriginal) {
            if (m.getNome().toLowerCase().contains(termo)) {
                modeloTabela.addRow(new Object[]{m.getNome(), m.getCrm(), m.getEspec(), m.getId()});
            }
        }

        if (termo.isEmpty()) {
            atualizarTabela();
        }
    }


