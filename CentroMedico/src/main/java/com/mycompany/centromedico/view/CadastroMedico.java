    private void btnEntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEntrarActionPerformed
        String nome = txtNome.getText();
        String especie = txtEspecialidade.getText();
        String idadeTexto = txtCrm.getText();

        if (nome.isEmpty() || especie.isEmpty() || idadeTexto.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Preencha todos os campos!");
            return;
        }

        try {
            int idade = Integer.parseInt(idadeTexto);
            controller.cadastrar(nome, especie, idade);

            // Atualiza a tabela
            modeloTabela.setRowCount(0); //Limpa
            ArrayList<Medico> lista = controller.listar();
            for (Medico m : lista) {
                modeloTabela.addRow(new Object[]{m.getNome(), m.getCrm(), m.getEspec()});
            }

            txtNome.setText("");
            txtEspecialidade.setText("");
            txtCrm.setText("");

        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(null, "crm Inválida");
        }
    }//GEN-LAST:event_btnEntrarActionPerformed

    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
        int linha = tbLista.getSelectedRow();

        if (linha == -1) {
            JOptionPane.showMessageDialog(null, "Selecione um animal na tabela para alterar.");
            return;
        }

        String novoNome = txtNome.getText();
        String novaEspecialidade = txtEspecialidade.getText();
        String novaCRM = txtCrm.getText();

        if (novoNome.isEmpty() || novaCRM.isEmpty() || novaEspecialidade.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Preecha os campos!");
            return;
        }

        try {
            int novaIdade = Integer.parseInt(novaCRM);

            // Atualiza a lista 
            ArrayList<Medico> lista = controller.listar();
            Medico m = lista.get(linha);
            m.setNome(novoNome);
            m.setEspec(novaEspecialidade);
            m.setCrm(novaCRM);

            // Atualiza a tabela
            modeloTabela.setValueAt(novoNome, linha, 0);
            modeloTabela.setValueAt(novaEspecialidade, linha, 1);
            modeloTabela.setValueAt(novaCRM, linha, 2);

            JOptionPane.showMessageDialog(null, "medico  atualizado com sucesso!");

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "crm  inválida");

        }
    }//GEN-LAST:event_btnAlterarActionPerformed

